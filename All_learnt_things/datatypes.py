# NUMBERS

# int datatype
a = 2
b = -1
print(type(b))

# float datatype
c = 1.2
d = -2.1
e = 12/5
print(type(e))

# complex datatype
f = 3+4j
g = -3-4j
print(type(f))

# String datatype
h = "Sj"
i = 'Sj  is " Batman" '
print(i)
print(type(i))

# Boolean datatype
j = True
k = False
print(type(j))

# None Datatype
l = None
print(type(l))